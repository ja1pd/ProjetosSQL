use classicmodels;

delimiter $

create procedure P_LISTA_CLIENTES(INOUT LISTA_CLIENTES_CLASSIC TEXT)
BEGIN

DECLARE DONE BOOL;
DECLARE VAR_CLIENTE VARCHAR(100) DEFAULT "";

DECLARE CURSOR_LISTA_CLIENTES CURSOR FOR 
	SELECT
		CUSTOMERNAME
	FROM 
		CUSTOMERS;

DECLARE CONTINUE HANDLER FOR
	NOT FOUND 
		SET
			DONE = TRUE;

OPEN CURSOR_LISTA_CLIENTES;

SET VAR_CLIENTE = " ";

LISTA: LOOP

	FETCH CURSOR_LISTA_CLIENTES INTO VAR_CLIENTE;
    
    IF 
		DONE = TRUE
	THEN
		LEAVE LISTA;
	END IF;
    
    SET LISTA_CLIENTES_CLASSIC =  CONCAT(LISTA_CLIENTES_CLASSIC, " ; ", VAR_CLIENTE);

END LOOP;

CLOSE CURSOR_LISTA_CLIENTES;

END$

DELIMITER ;

DROP PROCEDURE P_LISTA_CLIENTES;

SET @CLIENTE = "";
CALL P_LISTA_CLIENTES(@CLIENTE);

SELECT
	*
FROM
	CUSTOMERS
;

SELECT @CLIENTE;